#string
a  = "hello world"
print(type(a))

#Number
b  = 9
print(type(b))

#Float
c  = 9.9
print(type(c))

#list
List = ["abc",8,9.9,9+1j]
print(List)
print(type(List))

#range 
e = range(12)
print(e)
print(list(e))
print(type(list(e)))

#tuple
t = ("abc",8,9.9,9+1j)
print(t)
print(type(t))

e = range(12)
print(e)
print(tuple(e))
print(type(tuple(e)))

#Dictionary
d={'fruit':"mango",'number':"13"}
print(d)
print(type(d))
print(d.keys())
print(d.values())

#set
s = {"abc",8,9.9,9+1j}
print(s)
print(type(s))

